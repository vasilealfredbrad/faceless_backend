#!/usr/bin/env node
/**
 * Pre-generates one MP3 demo file per voice and saves them
 * to frontend/public/demos/<voice>.mp3 for instant static serving.
 *
 * Usage: UNREALSPEECH_API_KEY=... node scripts/generate-voice-demos.mjs
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT_DIR = path.resolve(__dirname, "../frontend/public/demos");

const API_KEY = process.env.UNREALSPEECH_API_KEY;
if (!API_KEY) {
  console.error("FATAL: Set UNREALSPEECH_API_KEY env var");
  process.exit(1);
}

const VOICES = [
  "Autumn", "Melody", "Hannah", "Emily", "Ivy", "Kaitlyn", "Luna", "Willow", "Lauren", "Sierra",
  "Noah", "Jasper", "Caleb", "Ronan", "Ethan", "Daniel", "Zane",
  "Mei", "Lian", "Ting", "Jing",
  "Wei", "Jian", "Hao", "Sheng",
  "Lucía", "Mateo", "Javier",
  "Élodie",
  "Ananya", "Priya", "Arjun", "Rohan",
  "Giulia", "Luca",
  "Camila", "Thiago", "Rafael",
];

const DEMO_TEXT =
  "What if one tiny habit changed everything? Most people overlook this simple trick, but it can make a real difference. Give it a try today.";

fs.mkdirSync(OUT_DIR, { recursive: true });

async function synthesize(voice) {
  const auth = API_KEY.startsWith("Bearer ") ? API_KEY : `Bearer ${API_KEY}`;
  const res = await fetch("https://api.v8.unrealspeech.com/speech", {
    method: "POST",
    headers: {
      Authorization: auth,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({
      Text: DEMO_TEXT,
      VoiceId: voice,
      Bitrate: "192k",
      Speed: "0",
      Pitch: "1.0",
      TimestampType: "sentence",
    }),
  });

  if (!res.ok) {
    const text = await res.text();
    throw new Error(`API ${res.status}: ${text}`);
  }

  const data = await res.json();
  if (!data?.OutputUri) throw new Error("No OutputUri in response");
  return data.OutputUri;
}

async function downloadToFile(url, dest) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Download failed: ${res.status}`);
  const buf = Buffer.from(await res.arrayBuffer());
  fs.writeFileSync(dest, buf);
}

function safeFilename(voice) {
  return voice
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-zA-Z0-9]/g, "_")
    .toLowerCase();
}

let ok = 0;
let fail = 0;

for (const voice of VOICES) {
  const fname = safeFilename(voice) + ".mp3";
  const dest = path.join(OUT_DIR, fname);

  if (fs.existsSync(dest)) {
    console.log(`SKIP ${voice} → ${fname} (already exists)`);
    ok++;
    continue;
  }

  try {
    process.stdout.write(`Generating ${voice}...`);
    const uri = await synthesize(voice);
    await downloadToFile(uri, dest);
    const size = (fs.statSync(dest).size / 1024).toFixed(0);
    console.log(` OK (${size} KB) → ${fname}`);
    ok++;
  } catch (err) {
    console.log(` FAIL: ${err.message}`);
    fail++;
  }

  // small delay to avoid rate limits
  await new Promise((r) => setTimeout(r, 500));
}

console.log(`\nDone: ${ok} ok, ${fail} failed out of ${VOICES.length}`);

// Generate the mapping file for the frontend
const mapping = {};
for (const voice of VOICES) {
  const fname = safeFilename(voice) + ".mp3";
  if (fs.existsSync(path.join(OUT_DIR, fname))) {
    mapping[voice] = `/demos/${fname}`;
  }
}
const tsContent = `// Auto-generated by scripts/generate-voice-demos.mjs
const VOICE_DEMO_FILES: Record<string, string> = ${JSON.stringify(mapping, null, 2)};
export default VOICE_DEMO_FILES;
`;
const tsPath = path.resolve(__dirname, "../frontend/src/lib/voice-demos.ts");
fs.writeFileSync(tsPath, tsContent);
console.log(`Wrote mapping to ${tsPath}`);
